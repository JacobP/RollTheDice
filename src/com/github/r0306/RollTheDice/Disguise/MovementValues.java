package com.github.r0306.RollTheDice.Disguise;

public class MovementValues
{
  int x;
  int y;
  int z;
  int yaw;
  int pitch;

  public MovementValues(int x, int y, int z, int yaw, int pitch)
  {
    this.x = x;
    this.y = y;
    this.z = z;
    this.yaw = yaw;
    this.pitch = pitch;
  }
}